export type Translations = Record<string, string | string[]>;

interface LangContextType {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: Translations;
}

const LangContext = createContext<LangContextType>({
  lang: "en",
  setLang: () => {},
  t: t.en,
});

export function LangProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(() => {
    const saved = localStorage.getItem("bflap_lang");
    return (saved === "zh" ? "zh" : "en") as Lang;
  });

  useEffect(() => {
    localStorage.setItem("bflap_lang", lang);
  }, [lang]);

  return (
    <LangContext.Provider value={{ lang, setLang, t: t[lang] }}>
      {children}
    </LangContext.Provider>
  );
}

export function useLang() {
  return useContext(LangContext);
}

export function useTranslation() {
  const { t: translations } = useContext(LangContext);
  const tFn = (key: string): string => {
    const val = translations[key];
    if (typeof val === "string") return val;
    return key;
  };
  return { t: tFn };
}
